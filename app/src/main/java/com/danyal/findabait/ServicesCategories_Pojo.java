package com.danyal.findabait;

public class ServicesCategories_Pojo {

    private int ID;
    private String name;
    private String logo;

    public int getID() {
        return ID;
    }
    public void setID (int ID)
    {
        this.ID = ID;
    }

    public String getImage() {
        return logo;
    }
    public void setImage (String logo)
    {
        this.logo = logo;
    }

    public String getName() {
        return name;
    }
    public void setName (String name)
    {
        this.name = name;
    }

}
